+(nullable NSString *)quDiaoTeShuZiFu:(nullable NSString *)string;
{
    
    NSString* strName=string;
    
    strName = [strName stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];  //去除掉首尾的空白字符和换行字符
    strName = [strName stringByReplacingOccurrencesOfString:@"\r" withString:@""];
    strName = [strName stringByReplacingOccurrencesOfString:@"\n" withString:@""];
    strName = [strName stringByReplacingOccurrencesOfString:@" " withString:@""];
    
    return strName;
    
}


